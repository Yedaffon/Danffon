# 计时器:
import time
import functools


def timer(func):
    @functools.wraps(func)
    def wrapper(*args, **kwargs):
        start = time.time()
        res = func(*args, **kwargs)
        end = time.time()
        cost = end - start
        print("耗时: {:.3f}s".format(cost))
        return res
    return wrapper
